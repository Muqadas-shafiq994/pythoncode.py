#escape sequence
#\n    for "new line"
print("muqadas\nshafiq")
print("hello\nworld")

#\t   for   "tab space"
print("muqadas\tshafiq")
print("hello\tworld")

#\\   for  backslash
print("muqadas\\shafiq")
print("hello\\world")

#\'    for single quote
print("muqadas\'shafiq")
print("hello\'world")

#\"   for   double quote
print("muqadas\"shafiq")
print("hello\"world")

#\b  for remove backspace character
print("muqadas\bshafiq")

